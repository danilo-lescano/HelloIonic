import { Storage } from '@ionic/storage';
import { Injectable } from '@angular/core';

export interface ICreature {
	id: number;
	isPlayer: boolean;
	name: string;
	initiative: string;
	hp: string;
}

@Injectable()
export class CreatureService {
    private creatures: ICreature[] = [];
    private creatureCount: number = 0;

    constructor(private storage: Storage){
        //this.storage.clear();
        this.load();
    }

    async load(){
        await this.onceRoutine();

        var creaturesAux = await this.storage.get("creatures");
        var creatureCountAux = await this.storage.get("creatureCount");

        if(creaturesAux === null || creatureCountAux === null){
            this.storage.set('creatures', this.creatures);
            this.storage.set('creatureCount', this.creatureCount);
        }
        else{
            this.storage.get('creatures').then(creatures => this.creatures = creatures);
            this.storage.get('creatureCount').then(creatureCount => this.creatureCount = creatureCount);
        }
    }

    async addCreature(creature: ICreature){
        if(creature.id === null)
            creature.id = ++this.creatureCount;
        
        var isEdited = false;
        for (let i = 0; i < this.creatures.length && !isEdited; i++) {
            if(creature.id == this.creatures[i].id){
                isEdited = true;
                this.creatures[i] = creature;
            }
        }
        if(!isEdited)
            this.creatures.push(creature);
        this.storage.set('creatures', this.creatures);
        this.storage.set('creatureCount', this.creatureCount);
        return;
    }
    delCreature(creature: ICreature){
        var isEdited = false;
        for (let i = 0; i < this.creatures.length && !isEdited; i++) {
            if(creature.id == this.creatures[i].id){
                isEdited = true;
                this.creatures.splice(i, 1);
            }
        }
        this.storage.set('creatures', this.creatures);
    }
    async getCreature(){
        this.creatures = await this.storage.get('creatures');
        if(this.creatures === null)
            this.creatures = [];
        return this.creatures.slice();
    }
    async getLastCreature(){
        let returnCreature: ICreature;
        this.creatures = await this.storage.get('creatures');
        this.creatureCount = await this.storage.get('creatureCount');
        for (let i = 0; this.creatures && i < this.creatures.length; i++) {
            if(this.creatureCount === this.creatures[i].id)
                return this.creatures[i];
        }
        return null;
    }

    async onceRoutine(){
        var onceRoutineFlag = await this.storage.get('onceRoutineFlag');
        if(!onceRoutineFlag){
            var fixedList = FixedList;
            for (let i = 0; i < fixedList.length; i++) {
                this.creatures.push({
                    id: ++this.creatureCount,
                    isPlayer: false,
                    name: fixedList[i].name,
                    hp: fixedList[i].hp,
                    initiative: fixedList[i].initiative,
                });
            }
            this.storage.set('creatures', this.creatures);
            this.storage.set('creatureCount', this.creatureCount);
            this.storage.set('onceRoutineFlag', true);
        }
    }
}


